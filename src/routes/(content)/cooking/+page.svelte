<h2>This is not your simple diet, since I need to satify the following conditions:</h2>
<ol class="list-disc px-6">
	<li class="font-sans text-justify">
		<span class="font-bold text-red-400">Finetuned calories counting</span>: If I am cutting and
		training, I will need to stay within -400 to -200 calories depending the phase and program of
		training. That means on a off day, I could be on 1500 calories!
	</li>
	<li class="font-sans text-justify">
		<span class="font-bold text-green-400">Balanced macro</span>: Getting enough protein is crucial;
		Fat needs to be limited to I cannot just spam butter to make things tasty; Carbs need to be
		timed around training.
	</li>
	<li class="font-sans text-justify">
		<span class="font-bold text-blue-400">Tasty and enjoyable</span>: A good diet is a sustainable
		diet. We are talking about a 3 years eating plan. I have to love what I eat, otherwise I will
		not stick to it.
	</li>
	<li class="font-sans text-justify">
		<span class="font-bold text-yellow-400">Affordable and reasonable to prepare</span>: I am not
		infinitely rich, so I need to be mindful with sourcing the ingredients. Also, sometimes after
		training I am so tired that I cannot spend another 2 hours cooking a meal, so they have to be
		reasonably cheap and easy to make. Tho I can have more elaborate meals from time to time.
	</li>
</ol>

<p>
	For anyone who has experience with dieting <span class="font-bold">WHILE</span> training will know
	how difficult it is to cut to around 5-7% body fat while maintaining performance. The most
	critical part of a successful diet is <span class="font-bold">NOT</span> to rely on will power, but
	smart planning and preparation. This page is dedicated to tracking my progress, sharing my receipes,
	and experiences along the way.
</p>

